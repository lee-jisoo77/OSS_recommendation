def get_questions() -> list[dict]:
    """Streamlit에 전달할 질문 목록 반환"""
    return [
        {
            "id": q["id"],
            "question": q["question"],
            "options": list(q["options"].keys())
        }
        for q in QUESTIONS
    ]


def recommend(answers: dict, top_n: int = 5) -> dict:
    """
    answers: {"mood": "선택지 문자열", "companion": "...", ...}
    반환:
    {
        "type": "유형명",
        "description": "유형 설명",
        "recommendations": [
            {"title": ..., "genre": ..., "director": ..., "year": ..., "summary": ...},
            ...
        ]
    }
    """
    # 유형별 점수 합산
    type_scores: defaultdict = defaultdict(int)
    for q in QUESTIONS:
        answer = answers.get(q["id"])
        if answer and answer in q["options"]:
            for mtype, weight in q["options"][answer].items():
                type_scores[mtype] += weight

    # 최고 유형 결정
    if not type_scores:
        best_type = random.choice(list(MOVIE_TYPES.keys()))
    else:
        best_type = max(type_scores, key=lambda k: type_scores[k])

    # 해당 유형 버킷에서 후보 추출
    candidates = list(TYPE_BUCKETS.get(best_type, []))

    # 부족하면 2위 유형 후보 추가
    if len(candidates) < top_n and type_scores:
        sorted_types = sorted(type_scores, key=lambda k: type_scores[k], reverse=True)
        if len(sorted_types) > 1:
            candidates += list(TYPE_BUCKETS.get(sorted_types[1], []))

    # 여전히 부족하면 전체에서
    if len(candidates) < top_n:
        candidates = MOVIES

    picked = random.sample(candidates, min(top_n, len(candidates)))

    return {
        "type": best_type,
        "description": MOVIE_TYPES[best_type],
        "recommendations": [
            {
                "title": m["title"],
                "genre": m["genre"],
                "director": m["director"],
                "year": m["year"],
                "summary": m["summary"],
            }
            for m in picked
        ]
    }