def _classify_movie(movie: dict) -> defaultdict:
    """summary + genre 키워드로 영화의 감정 유형 점수 산출"""
    summary = movie.get("summary", "")
    genre = movie.get("genre", "")
    scores = defaultdict(int)

    for mtype, keywords in EMOTION_KEYWORDS.items():
        for kw in keywords:
            if kw in summary:
                scores[mtype] += 2

    for mtype, genres in GENRE_WEIGHTS.items():
        for g in genres:
            if g in genre:
                scores[mtype] += 3

    return scores


def _assign_type(movie: dict) -> str:
    """영화 하나에 대표 유형 1개 부여"""
    scores = _classify_movie(movie)
    if not scores:
        return "보고 나서 멍때리는형"
    return max(scores, key=lambda k: scores[k])


